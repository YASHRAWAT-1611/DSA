// Variables and Data Types

#include <iostream>
using namespace std;

int main()
{
    int a = 11; // integer has 4 bytes
    double b = 11.55; // double has 4 bytes
    char c = 'Y'; // char has 1 bytes
    string d = "Hello World";  
    bool f = true; // boolean has 1 byte

    cout << a << endl;
    cout << b << endl;
    cout << c << endl;
    cout << d << endl;
    cout << f << endl;

    return 0;
}